char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	k;

	k = 0;
	i = 0;
	if (to_find[i] == '\0')
		return (str);
	while (str[i] != '\0')
	{
		k = 0;
		while (to_find[k] == str[i + k])
		{
			if (to_find[k + 1] == '\0')
			{
				return (&str[i]);
			}
			k++;
		}
		i++;
	}
	return (0);
}
